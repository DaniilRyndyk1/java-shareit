DROP TABLE IF EXISTS booking;
DROP TABLE IF EXISTS comments;
DROP TABLE IF EXISTS item;
DROP TABLE IF EXISTS item_request;
DROP TABLE IF EXISTS users;
CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);
CREATE TABLE IF NOT EXISTS item_request (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  description VARCHAR(512) NOT NULL,
  requestor_id BIGINT REFERENCES users (id) NOT NULL,
  created TIMESTAMP NOT NULL,
  CONSTRAINT pk_item_request PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS item (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(512) NOT NULL,
  available BOOLEAN NOT NULL,
  owner_id BIGINT REFERENCES users (id) NOT NULL,
  item_request_id BIGINT REFERENCES item_request (id),
  CONSTRAINT pk_item PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS booking (
id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
start_date TIMESTAMP,
end_date TIMESTAMP,
item_id BIGINT REFERENCES item (id) NOT NULL,
booker_id BIGINT REFERENCES users (id) NOT NULL,
review VARCHAR(1024),
status VARCHAR(255) NOT NULL,
CONSTRAINT pk_booking PRIMARY KEY (id)
);
CREATE TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  text VARCHAR(2048) NOT NULL,
  item_id BIGINT REFERENCES item (id) NOT NULL,
  author_id BIGINT REFERENCES users (id) NOT NULL,
  created TIMESTAMP NOT NULL,
  CONSTRAINT pk_comment PRIMARY KEY (id)
);